            <h2>📚 Bibliothèque numérique</h2>
            <p>
                La bibliothèque numérique met à la disposition 
                des élèves et des enseignants une collection d'ouvrages pédagogiques accessibles en ligne.
                Grâce à un moteur de recherche intuitif, les utilisateurs peuvent retrouver rapidement 
                un livre en effectuant une recherche par titre, auteur, ISBN ou année de publication. 
                Une fois authentifiés, ils peuvent consulter les informations détaillées sur chaque ouvrage
                et télécharger les ressources mises à leur disposition afin de faciliter leurs apprentissages et leurs travaux académiques.
            </p>
        </div>
        <div>
            <h2>📅 Emplois du temps</h2>
            <p>
                Ce module permet une gestion centralisée des emplois du temps de l'établissement. 
                L'administrateur peut créer et organiser les séances de cours en associant les classes,
                les enseignants, les matières ainsi que les horaires correspondants. Les élèves et 
                les enseignants disposent ensuite d'un accès personnalisé leur permettant de consulter
                leur emploi du temps en temps réel, réduisant ainsi les risques de confusion et 
                améliorant l'organisation des activités scolaires.
            </p>
        </div>
        <div>
            <h2>👨‍🏫 Gestion des enseignants</h2>
            <p>
                La plateforme facilite l'administration des enseignants en permettant la création et
                la gestion de leurs profils. L'administrateur peut enregistrer les informations 
                essentielles de chaque enseignant, notamment son matricule, sa spécialité et ses 
                coordonnées, puis l'associer aux différentes matières et emplois du temps. 
                Cette centralisation simplifie le suivi du personnel enseignant et garantit une
                meilleure organisation des ressources humaines de l'établissement.
            </p>
        </div>
        <div>
            <h2>🎓 Gestion des élèves</h2>
            <p>
                Le module de gestion des élèves permet de centraliser toutes les informations administratives 
                relatives aux apprenants. L'administrateur peut créer les comptes des élèves, les affecter à 
                leurs classes respectives et gérer leurs informations personnelles. Une fois connectés, 
                les élèves bénéficient d'un espace personnel leur donnant accès à leur emploi du temps 
                ainsi qu'aux ressources disponibles dans la bibliothèque numérique, favorisant ainsi un environnement 
                d'apprentissage moderne et organisé.
            </p>
        </div>
